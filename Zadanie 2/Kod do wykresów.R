calculateNormForVector<-function(vector){
  return(sum(vector))
}

calculateNormForMatrix<-function(matrix){
  lengthOfColumn<-sqrt(length(matrix))
  res<-c()
  for (i in 1:lengthOfColumn){
    a<-lengthOfColumn*(i-1)+1
    b<-lengthOfColumn*i
    res<-append(res,sum(matrix[a:b]))
  }
  return(max(res))
}

